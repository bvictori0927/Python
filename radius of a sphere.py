import math
r = float(input("Enter radius of sphere: "))
diameter = 2*r
circumference = 2*math.pi*r
area = 4 * math.pi * r * r
volume = (4 / 3) * math.pi * r ** 3
print("Diameter =", diameter)
print("Circumference =",circumference)
print("Surface area =",area)
print("Volume =", volume)







